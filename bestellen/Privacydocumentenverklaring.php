	<h1>Privacy-verklaring  Vocaal ensemble Trajecti Voces</h1>
	<p class="c0"><span class="c4"></span>
	</p>
	<p class="c6"><span class="c4">Het koor draagt de naam Trajecti Voces en is opgericht te Utrecht op 5 maart 1989 als vereniging met rechtspersoonlijkheid. Het bestuur is verantwoordelijk voor de verwerking van persoonsgegevens zoals weergegeven in deze privacyverklaring.</span>
	</p>
	<p class="c0">&nbsp;</p>
	<p class="c0"><span class="c1">Functionaris gegevensbescherming</span>
	</p>
	<p class="c6"><span class="c4">Trajecti Voces heeft geen vaste &ldquo;functionaris gegevensbescherming&rdquo;. Deze wordt binnen de vereniging door het bestuur benoemd. Gezien de aard, doelstelling en omvang van de vereniging kan de verwerking van persoonsgegevens bij Trajecti Voces als laag complex gezien worden.</span>
	</p>
	<p class="c0">&nbsp;</p>
	<p class="c0"><span class="c1">Persoonsgegevens die wij verwerken</span>
	</p>
	<ol class="c8 lst-kix_list_1-1 start" start="1">
